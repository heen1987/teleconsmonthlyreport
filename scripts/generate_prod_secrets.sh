    fi
}

# macOS sed ? GNU sed 紐⑤몢 吏?먰븯??移섑솚 ?⑥닔
set_env_var() {
    local file="$1"
    local key="$2"
    local value="$3"
    # ?ㅺ? ?대? ?덉쑝硫?援먯껜, ?놁쑝硫?異붽?
    if grep -q "^${key}=" "$file" 2>/dev/null; then
        sed -i.sedbak "s|^${key}=.*|${key}=${value}|" "$file"
        rm -f "${file}.sedbak"
    else
        echo "${key}=${value}" >> "$file"
    fi
}

